function renderFavorites() {
  const container = document.getElementById("favorite-list");
  const favorites = JSON.parse(localStorage.getItem("favorites") || "[]");

  const matched = recipes.filter(r => favorites.includes(r.name));
  if (matched.length === 0) {
    container.innerHTML = "<p>目前尚未收藏任何食譜。</p>";
    return;
  }

  container.innerHTML = matched.map(recipe => `
    <div class="card">
      <h2>${recipe.name}</h2>
      <h4>🍽 所需食材：</h4>
      <ul>${recipe.ingredients.map(ing => `<li>${ing}</li>`).join("")}</ul>
      <h4>📝 步驟：</h4>
      <ul>${recipe.steps.map(step => `<li>${step}</li>`).join("")}</ul>
    </div>
  `).join("<hr>");
}

window.onload = renderFavorites;
